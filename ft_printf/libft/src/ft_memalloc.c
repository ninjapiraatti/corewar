#include "../includes/libft.h"

void	*ft_memalloc(size_t size)
{
	void *mem;

	if ((mem = (void *)malloc(size * sizeof(*mem))))
	{
		ft_bzero(mem, size);
		return (mem);
	}
	else
		return (NULL);
}
